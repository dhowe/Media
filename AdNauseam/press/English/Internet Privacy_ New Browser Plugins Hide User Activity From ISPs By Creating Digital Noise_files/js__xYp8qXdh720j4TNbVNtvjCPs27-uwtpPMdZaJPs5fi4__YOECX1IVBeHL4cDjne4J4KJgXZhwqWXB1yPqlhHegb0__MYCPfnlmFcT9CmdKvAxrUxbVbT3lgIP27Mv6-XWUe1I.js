(function($) {

  Drupal.behaviors.breakingNews = {
    attach: function (context, settings) {
      var $prev_num = 1;
      var $num = 1;
      var $total = 0;
      var $timeOut="";
      var $timeOutDelay=true;
      var $pause=false;
      function rotateTicker() {
        $total = $('.breaking-news li').length;
        $('.breaking-news li').hide();
        $('.breaking-news li').first().css('display', 'inline');
      }

      function autoStart(){
        if($timeOutDelay){$timeOutDelay=false;}
        else nextTicker();
        $timeOut = setTimeout(autoStart, 3000);
      }

      function nextTicker(){
        if($timeOut) clearTimeout($timeOut);
        $prev_num = $num;
        $num = $num+1;
        if($num>$total)$num=1;
        changeTicker();
      }

      function prevTicker(){
        if($timeOut) clearTimeout($timeOut);
        $prev_num = $num;
        $num = $num-1;
        if($num<1)$num=$total;
        changeTicker();
      }

      function pauseTicker(){
        if($timeOut) clearTimeout($timeOut);
      }

      function changeTicker(){
        //fade effect
        if ($total > 1) {
          $(".breaking-news li:nth-child("+$prev_num+")").fadeOut('fast',function (){
              $(".breaking-news li:nth-child("+$num+")").fadeIn('slow');
          });
        }
      }

      $(document).ready(function(){
        if ($('.breaking-news').length && !$.cookie($('.breaking-news').attr('id'))) {
          $('body').addClass('breakingNewsLoaded');
          $('.breaking-news').slideDown(function() {
            $.event.trigger({
              type: 'breaking_news_open'
            });
          });
        }
        // If breaking news cookie with id is unset
          // Display breaking news
        $('.breaking-news li').hover(function (){
            pauseTicker();
        });
        $('.breaking-news li').mouseleave(function (){
            $timeOutDelay=true;
            if(!$pause)autoStart();
        });
        $('.breaking-news .close').on('click', function() {
          $('body').removeClass('breakingNewsLoaded');
          $.cookie($('.breaking-news').attr('id'), true);
          $('.breaking-news').slideUp(function() {
            $.event.trigger({
              type: 'breaking_news_close'
            });
          });
        });
        rotateTicker();
        autoStart();
      });
    }
  }
})(jQuery)
